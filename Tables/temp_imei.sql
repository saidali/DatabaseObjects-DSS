CREATE TABLE DSS.TEMP_IMEI
(
  ROWSEQ        INTEGER,
  SERIALNUMBER  NVARCHAR2(150),
  PARENTKEY     NVARCHAR2(150),
  PRODUCTCODE   NVARCHAR2(100),
  ISPARENT      CHAR(1 BYTE),
  HASCHILD      CHAR(1 BYTE)
)
TABLESPACE AXM_TS_WMS
PCTUSED    0
PCTFREE    10
INITRANS   1
MAXTRANS   255
STORAGE    (
            INITIAL          64K
            NEXT             1M
            MINEXTENTS       1
            MAXEXTENTS       UNLIMITED
            PCTINCREASE      0
            BUFFER_POOL      DEFAULT
           )
LOGGING 
NOCOMPRESS 
NOCACHE
MONITORING;


CREATE UNIQUE INDEX DSS.TEMP_IMEI_PK ON DSS.TEMP_IMEI
(ROWSEQ, SERIALNUMBER)
LOGGING
